package stage1;

public class Operations {

    public static void main(String[] args){
        int x = 10;
        float y = 5.55f;
        float sum = x + y;
        float sub = x - y;
        float mult = x*y;
        float div = x/y;
        System.out.println("Сложение: "+ sum);
        System.out.println("Вычитание: "+ sub);
        System.out.println("Умножение: "+ mult);
        System.out.println("Деление: "+ div);
    }
}
